# COSINE SIMILARITY FUNCTION
# ============================================================================
def cosine_similarity(v1: np.ndarray, v2: np.ndarray, label: str = "") -> float:
    """
    Compute cosine similarity between two L2-normalized embeddings.
    
    Args:
        v1: First embedding vector
        v2: Second embedding vector
        label: Optional label for printing
        
    Returns:
        Cosine similarity value (between -1 and 1)
    """
    # Verify vectors are normalized (should be close to 1.0)
    n1 = np.linalg.norm(v1)
    n2 = np.linalg.norm(v2)
    
    print(f"  Norms: {n1:.8f}, {n2:.8f}")
    
    # Compute dot product (cosine similarity for normalized vectors)
    sim = np.dot(v1, v2)
    
    # Clamp to valid range due to floating point errors
    sim = max(-1.0, min(1.0, sim))
    
    if label:
        print(f"  Cosine similarity [{label}] : {sim:.8f}")
    
    return sim

